var1 = ' '  # A string containing a space
var2 = '[ DS , ML , Python]'  # A string (because it's enclosed in quotes)
var3 = ['DS', 'ML', 'Python']  # A list of strings
var4 = 1.  # A float (decimal point makes it a float)

# Checking the types
print(type(var1))  # Output: <class 'str'>
print(type(var2))  # Output: <class 'str'>
print(type(var3))  # Output: <class 'list'>
print(type(var4))  # Output: <class 'float'>
